    protected static function boot()
    {
        parent::boot();

        static::creating(function ($category) {
            if (empty($category->slug)) {
                $category->slug = Str::slug($category->name);
            }
        });
    }

    public function jobs(): HasMany
    {
        return $this->hasMany(Job::class);
    }

    public function activeJobs(): HasMany
    {
        return $this->hasMany(Job::class)
            ->where('is_active', true)
            ->where(function ($q) {
                $q->whereNull('closed_at')
                  ->orWhere('closed_at', '>=', now());
            });
    }

    public function scopeActive($query)
    {
        return $query->where('is_active', true)->orderBy('order');
    }

    public function scopeInactive($query)
    {
        return $query->where('is_active', false);
    }

    public function getActiveJobsCountAttribute(): int
    {
        return $this->activeJobs()->count();
    }

    public function getTotalJobsCountAttribute(): int
    {
        return $this->jobs()->count();
    }

    // Get SVG path for icon
    public function getIconSvgPathAttribute(): string
    {
        return self::$iconPaths[$this->icon] ?? self::$iconPaths['briefcase'];
    }

    // Render icon HTML
    public function renderIcon($class = 'w-6 h-6'): string
    {
        $path = $this->icon_svg_path;
        return '<svg class="' . $class . '" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="' . $path . '"/></svg>';
    }
